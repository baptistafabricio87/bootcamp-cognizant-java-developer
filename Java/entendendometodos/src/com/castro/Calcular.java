package com.castro;

public class Calcular {

    protected int somar(int valor1, int valor2){
        return valor1 + valor2;
    }
    protected int subtrair(int valor1, int valor2){
        return valor1 - valor2;
    }
    protected double multiplicar(double valor1, double valor2){
        return valor1 * valor2;
    }
    protected double dividir(double valor1, double valor2){
        return valor1 / valor2;
    }
}
